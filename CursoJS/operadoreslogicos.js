//Operadores logicos

let a = true;
let b = false;

console.log(a);
console.log(b);


//Operador logico && (and o Y)
//Regresa verdadero si ambos operandos son verdaderos
console.log(`${a} && ${b} -> ${a && b}`)

//OPerador logico || (or o O)
//Regresa verdadero si cualquiera de los valores es verdadero
console.log(`${a} || ${b} -> ${a || b}`)